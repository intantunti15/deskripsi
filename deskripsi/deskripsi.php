    <h1> DESKRIPSI PROJEK</h1>
    <h4> Nama Projek : Website Sepatu </h4>
    <h4> Deskripsi Singkat : Proyek ini bertujuan untuk membuat sebuah website yang berfokus pada penjualan sepatu secara online. Website ini akan menyediakan berbagai macam sepatu, mulai dari sepatu olahraga, sepatu kasual, hingga sepatu formal. Pengguna dapat melihat daftar sepatu yang tersedia, melihat detail produk, harga,dan stok</h4>
    <h4> Fitur yang tersedia</h4>
    <h4> 1.Beranda (Home): Menampilkan gambar-gambar menarik sepatu terbaru, penawaran khusus, dan pengenalan tentang brand sepatu yang dijual</h4>
    <h4> 2.Kategori Sepatu: Menampilkan kategori-kategori sepatu yang tersedia, seperti olahraga, kasual, formal, dan lain-lain.</h4>
    <h4> 3.Daftar Produk: Menampilkan daftar sepatu yang tersedia beserta gambar, harga, dan deskripsi singkat. Pengguna dapat melihat detail produk dengan mengklik salah satu sepatu.</h4>
    <h4> 4.Detail Produk: Menampilkan informasi lengkap tentang sebuah sepatu, termasuk gambar yang lebih besar, ukuran yang tersedia, fitur produk, dan ulasan pengguna.</h4>

